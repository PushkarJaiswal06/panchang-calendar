/**
 * Get festival for a given month and tithi.
 * @param hinduMonth - The Hindu month name (e.g., "चैत्र")
 * @param tithi - The full tithi name (e.g., "शुक्ल नवमी") or special tithi ("अमावस्या", "पूर्णिमा")
 * @returns Festival name or undefined
 */
export function getFestivalForTithi(hinduMonth: string, tithi: string): string | undefined {
  const monthFestivals = MONTH_FESTIVALS[hinduMonth];
  if (!monthFestivals) return undefined;
  return monthFestivals[tithi];
}

